length = int(input("Please enter the length (in cm): "))
width = int(input("Please enter the width (in cm): "))
height = int(input("Please enter the height (in cm):"))

print ("\n")

volume = length * width * height

print ("The length of the cuboid is: ",length, "cm")
print ("The width of the cuboid is: ",width , "cm")
print ("The height of the cuboid is: ",height, "cm")

print ("\n")

print("The volume of the cuboid is: ", volume, "cm")
